const buckets = [
  'campaigns-images',
  'events-images', 
  'team-photos',
  'partners-logos'
]

console.log('\n── Testing bucket LIST ──')
const { data: bucketList, error: listErr } = await supabase.storage.listBuckets()
if (listErr) {
  console.log('listBuckets error:', listErr.message)
} else {
  console.log('Visible buckets:', bucketList?.map(b => b.name).join(', ') || '(none)')
}

console.log('\n── Testing UPLOAD to each bucket ──')
for (const bucket of buckets) {
  const filename = `test-diagnostic-${Date.now()}.jpg`
  const { data, error } = await supabase.storage
    .from(bucket)
    .upload(filename, tiny1x1Jpeg, { contentType: 'image/jpeg', upsert: true })
  
  if (error) {
    console.log(`✗ ${bucket}: UPLOAD FAILED → ${error.message}`)
  } else {
    const { data: { publicUrl } } = supabase.storage.from(bucket).getPublicUrl(filename)
    console.log(`✓ ${bucket}: UPLOAD OK → ${publicUrl}`)
    // Clean up test file
    await supabase.storage.from(bucket).remove([filename])
  }
}

